# 7. Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário. 
# 
# Lembrando que: Area = l*l
#
lado = int(input("Digite o tamanho de um dos lados: "))
area = lado*lado
dobro = area*2
print("Área:", area)
print("Dobro:", dobro)